/*
    All 8 bit instructions

    d8  means immediate 8 bit data
    d16 means immediate 16 bit data
    a8  means 8 bit unsigned data, which are added to $FF00 in certain instructions (replacement for missing IN and OUT instructions)
    a16 means 16 bit address
    r8  means 8 bit signed data, which are added to program counter


*/

struct flags
{
    char zero;
    char subtract;
    char halfCarry;
    char carry;
};

struct duration
{
    duration() {}
    duration(u8 first, u8 second) : firstCondition(first), secondCondition(second) {};
    duration(u8 first) : firstCondition(first), secondCondition(0) {};

    u8 firstCondition;
    u8 secondCondition = 0;

};


static struct instruction
{
    instruction() {}
    instruction(char* n, u8 l, duration d, flags f) : name(n), length(l), duration(d), flags(f) {}

    char* name;         // Mnemonic
    u8 length;          // Bytes
    duration duration;  // Cycles
    flags flags;        // Flags affected

};

